"use client";

import React, { useState, useEffect } from "react";
import { useParams } from "next/navigation";
import { AnimatePresence, motion } from "framer-motion";
import {
  Download,
  Share2,
  Calendar,
  Mail,
  MessageSquare,
  CheckCircle,
  AlertTriangle,
  TrendingUp,
  Zap,
  Users,
  Clock,
  DollarSign,
} from "lucide-react";
import Link from "next/link";
import toast from "react-hot-toast";

interface BlueprintData {
  id: string;
  summary: string;
  recommendedStack: {
    frontend: string[];
    backend: string[];
    database: string[];
    infrastructure: string[];
    tools: string[];
  };
  phases: Array<{
    name: string;
    timeline: string;
    deliverables: string[];
    estimatedCost: string;
  }>;
  risks: Array<{
    type: string;
    description: string;
    mitigation: string;
  }>;
  successMetrics: string[];
  nextSteps: Array<{
    action: string;
    timeline: string;
    priority: string;
  }>;
  readinessScore: number;
  generatedAt: string;
}

export default function ResultsPage() {
  const params = useParams();
  const id = params.id as string;
  const [blueprint, setBlueprint] = useState<BlueprintData | null>(null);
  const [loading, setLoading] = useState(true);
  const [activeTab, setActiveTab] = useState("overview");

  useEffect(() => {
    fetchBlueprint();
  }, [id]);

  const fetchBlueprint = async () => {
    try {
      const response = await fetch(`/api/idea-clarity/results?id=${id}`);
      const data = await response.json();

      if (response.ok) {
        setBlueprint(data.blueprint);
      } else {
        throw new Error(data.error);
      }
    } catch (error) {
      console.error("Failed to fetch blueprint:", error);
      toast.error("Failed to load blueprint");
    } finally {
      setLoading(false);
    }
  };

  const handleDownload = () => {
    toast.success("PDF download started");
    // In production, generate and download PDF
  };

  const handleShare = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title: "My Project Blueprint",
          text: "Check out my project blueprint generated by Hassan & SOFTXIC",
          url: window.location.href,
        });
      } catch (error) {
        console.error("Error sharing:", error);
      }
    } else {
      navigator.clipboard.writeText(window.location.href);
      toast.success("Link copied to clipboard!");
    }
  };

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-center">
          <div className="h-12 w-12 border-4 border-blue-500 border-t-transparent rounded-full animate-spin mx-auto mb-4" />
          <p className="text-gray-600">Generating your blueprint...</p>
        </div>
      </div>
    );
  }

  if (!blueprint) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-center">
          <AlertTriangle className="h-12 w-12 text-yellow-500 mx-auto mb-4" />
          <h2 className="text-2xl font-bold mb-2">Blueprint Not Found</h2>
          <p className="text-gray-600 mb-4">
            The requested blueprint could not be found.
          </p>
          <Link
            href="/idea-clarity"
            className="inline-flex items-center px-6 py-3 rounded-lg bg-gradient-to-r from-blue-600 to-cyan-500 text-white font-medium"
          >
            Create New Blueprint
          </Link>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gradient-to-b from-gray-50 to-white dark:from-gray-900 dark:to-black">
      {/* Header */}
      <div className="bg-gradient-to-r from-blue-600 to-cyan-500 text-white">
        <div className="container mx-auto px-4 py-12">
          <div className="flex flex-col md:flex-row items-start md:items-center justify-between gap-6">
            <div>
              <h1 className="text-3xl md:text-4xl font-bold mb-4">
                Your Project Blueprint is Ready! üéâ
              </h1>
              <p className="text-blue-100 max-w-2xl">{blueprint.summary}</p>
            </div>

            <div className="flex flex-wrap gap-3">
              <button
                onClick={handleDownload}
                className="px-4 py-2 bg-white text-blue-600 rounded-lg font-medium hover:bg-gray-100 transition-colors flex items-center space-x-2"
              >
                <Download className="h-4 w-4" />
                <span>Download PDF</span>
              </button>
              <button
                onClick={handleShare}
                className="px-4 py-2 bg-blue-700 text-white rounded-lg font-medium hover:bg-blue-800 transition-colors flex items-center space-x-2"
              >
                <Share2 className="h-4 w-4" />
                <span>Share</span>
              </button>
            </div>
          </div>
        </div>
      </div>

      {/* Readiness Score */}
      <div className="container mx-auto px-4 -mt-8 relative z-10">
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          className="bg-white dark:bg-gray-900 rounded-2xl shadow-xl p-6 md:p-8"
        >
          <div className="grid md:grid-cols-2 lg:grid-cols-4 gap-6">
            <div className="text-center p-6 rounded-xl bg-gradient-to-br from-blue-50 to-cyan-50 dark:from-blue-900/20 dark:to-cyan-900/20">
              <div className="text-4xl font-bold text-gradient mb-2">
                {blueprint.readinessScore}/100
              </div>
              <div className="text-gray-600 dark:text-gray-400">
                Readiness Score
              </div>
              <div className="mt-2 text-sm">
                {blueprint.readinessScore >= 80 && (
                  <span className="text-green-600">üéØ Ready to start!</span>
                )}
                {blueprint.readinessScore >= 60 &&
                  blueprint.readinessScore < 80 && (
                    <span className="text-yellow-600">‚ö†Ô∏è Needs refinement</span>
                  )}
                {blueprint.readinessScore < 60 && (
                  <span className="text-red-600">
                    üö® Requires more planning
                  </span>
                )}
              </div>
            </div>

            <div className="text-center p-6 rounded-xl bg-gradient-to-br from-green-50 to-emerald-50 dark:from-green-900/20 dark:to-emerald-900/20">
              <Clock className="h-8 w-8 text-green-600 mx-auto mb-3" />
              <div className="text-2xl font-bold mb-1">
                {blueprint.phases.reduce((total, phase) => {
                  const weeks = parseInt(phase.timeline.split("-")[0]);
                  return total + (weeks || 2);
                }, 0)}{" "}
                weeks
              </div>
              <div className="text-gray-600 dark:text-gray-400">
                Estimated Timeline
              </div>
            </div>

            <div className="text-center p-6 rounded-xl bg-gradient-to-br from-purple-50 to-pink-50 dark:from-purple-900/20 dark:to-pink-900/20">
              <Users className="h-8 w-8 text-purple-600 mx-auto mb-3" />
              <div className="text-2xl font-bold mb-1">
                {blueprint.phases.length}
              </div>
              <div className="text-gray-600 dark:text-gray-400">
                Development Phases
              </div>
            </div>

            <div className="text-center p-6 rounded-xl bg-gradient-to-br from-orange-50 to-red-50 dark:from-orange-900/20 dark:to-red-900/20">
              <AlertTriangle className="h-8 w-8 text-orange-600 mx-auto mb-3" />
              <div className="text-2xl font-bold mb-1">
                {blueprint.risks.length}
              </div>
              <div className="text-gray-600 dark:text-gray-400">
                Identified Risks
              </div>
            </div>
          </div>
        </motion.div>
      </div>

      {/* Main Content */}
      <div className="container mx-auto px-4 py-12">
        <div className="grid lg:grid-cols-3 gap-8">
          {/* Left Column - Tabs */}
          <div className="lg:col-span-2">
            {/* Tab Navigation */}
            <div className="flex overflow-x-auto mb-8 border-b border-gray-200 dark:border-gray-800">
              {[
                {
                  id: "overview",
                  label: "Overview",
                  icon: <TrendingUp className="h-4 w-4" />,
                },
                {
                  id: "tech-stack",
                  label: "Tech Stack",
                  icon: <Zap className="h-4 w-4" />,
                },
                {
                  id: "phases",
                  label: "Phases",
                  icon: <Clock className="h-4 w-4" />,
                },
                {
                  id: "risks",
                  label: "Risks",
                  icon: <AlertTriangle className="h-4 w-4" />,
                },
                {
                  id: "next-steps",
                  label: "Next Steps",
                  icon: <CheckCircle className="h-4 w-4" />,
                },
              ].map((tab) => (
                <button
                  key={tab.id}
                  onClick={() => setActiveTab(tab.id)}
                  className={`flex items-center space-x-2 px-4 py-3 font-medium border-b-2 transition-all whitespace-nowrap ${
                    activeTab === tab.id
                      ? "border-blue-500 text-blue-600 dark:text-blue-400"
                      : "border-transparent text-gray-600 dark:text-gray-400 hover:text-gray-900 dark:hover:text-gray-300"
                  }`}
                >
                  {tab.icon}
                  <span>{tab.label}</span>
                </button>
              ))}
            </div>

            {/* Tab Content */}
            <AnimatePresence mode="wait">
              {activeTab === "overview" && (
                <motion.div
                  key="overview"
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -10 }}
                  className="space-y-8"
                >
                  <div>
                    <h3 className="text-2xl font-bold mb-4">Project Summary</h3>
                    <p className="text-gray-700 dark:text-gray-300">
                      {blueprint.summary}
                    </p>
                  </div>

                  <div>
                    <h3 className="text-2xl font-bold mb-4">Success Metrics</h3>
                    <div className="grid md:grid-cols-2 gap-4">
                      {blueprint.successMetrics.map((metric, index) => (
                        <div
                          key={index}
                          className="flex items-start space-x-3 p-4 rounded-lg bg-gray-50 dark:bg-gray-800/50"
                        >
                          <CheckCircle className="h-5 w-5 text-green-500 mt-0.5 flex-shrink-0" />
                          <span>{metric}</span>
                        </div>
                      ))}
                    </div>
                  </div>
                </motion.div>
              )}

              {activeTab === "tech-stack" && (
                <motion.div
                  key="tech-stack"
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -10 }}
                  className="space-y-8"
                >
                  {Object.entries(blueprint.recommendedStack).map(
                    ([category, technologies]) => (
                      <div key={category}>
                        <h3 className="text-xl font-bold mb-3 capitalize">
                          {category.replace(/([A-Z])/g, " $1")}
                        </h3>
                        <div className="flex flex-wrap gap-2">
                          {technologies.map((tech, index) => (
                            <span
                              key={index}
                              className="px-4 py-2 rounded-full bg-blue-100 dark:bg-blue-900/30 text-blue-700 dark:text-blue-400 font-medium"
                            >
                              {tech}
                            </span>
                          ))}
                        </div>
                      </div>
                    )
                  )}
                </motion.div>
              )}

              {activeTab === "phases" && (
                <motion.div
                  key="phases"
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -10 }}
                  className="space-y-8"
                >
                  {blueprint.phases.map((phase, index) => (
                    <div
                      key={index}
                      className="p-6 rounded-xl border border-gray-200 dark:border-gray-800 bg-white dark:bg-gray-900/50"
                    >
                      <div className="flex items-start justify-between mb-4">
                        <div>
                          <div className="flex items-center space-x-3 mb-2">
                            <div className="h-8 w-8 rounded-full bg-gradient-to-r from-blue-500 to-cyan-500 flex items-center justify-center text-white font-bold">
                              {index + 1}
                            </div>
                            <h3 className="text-xl font-bold">{phase.name}</h3>
                          </div>
                          <div className="flex items-center space-x-4 text-sm text-gray-600 dark:text-gray-400">
                            <span className="flex items-center space-x-1">
                              <Clock className="h-4 w-4" />
                              <span>{phase.timeline}</span>
                            </span>
                            <span className="flex items-center space-x-1">
                              <DollarSign className="h-4 w-4" />
                              <span>{phase.estimatedCost}</span>
                            </span>
                          </div>
                        </div>
                      </div>

                      <div className="space-y-3">
                        <h4 className="font-semibold">Deliverables:</h4>
                        <ul className="space-y-2">
                          {phase.deliverables.map((deliverable, idx) => (
                            <li
                              key={idx}
                              className="flex items-start space-x-2"
                            >
                              <CheckCircle className="h-4 w-4 text-green-500 mt-0.5 flex-shrink-0" />
                              <span>{deliverable}</span>
                            </li>
                          ))}
                        </ul>
                      </div>
                    </div>
                  ))}
                </motion.div>
              )}

              {activeTab === "risks" && (
                <motion.div
                  key="risks"
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -10 }}
                  className="space-y-6"
                >
                  {blueprint.risks.map((risk, index) => (
                    <div
                      key={index}
                      className="p-6 rounded-xl border border-red-200 dark:border-red-800 bg-red-50 dark:bg-red-900/10"
                    >
                      <div className="flex items-start space-x-3 mb-4">
                        <AlertTriangle className="h-6 w-6 text-red-500 flex-shrink-0" />
                        <div>
                          <div className="font-bold mb-1 capitalize">
                            {risk.type} Risk
                          </div>
                          <p className="text-gray-700 dark:text-gray-300">
                            {risk.description}
                          </p>
                        </div>
                      </div>
                      <div className="pl-9">
                        <div className="text-sm font-medium text-green-700 dark:text-green-400 mb-1">
                          Mitigation Strategy:
                        </div>
                        <p className="text-gray-600 dark:text-gray-400">
                          {risk.mitigation}
                        </p>
                      </div>
                    </div>
                  ))}
                </motion.div>
              )}

              {activeTab === "next-steps" && (
                <motion.div
                  key="next-steps"
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -10 }}
                  className="space-y-6"
                >
                  {blueprint.nextSteps.map((step, index) => (
                    <div
                      key={index}
                      className={`p-6 rounded-xl border ${
                        step.priority === "high"
                          ? "border-blue-300 dark:border-blue-700 bg-blue-50 dark:bg-blue-900/20"
                          : step.priority === "medium"
                          ? "border-yellow-300 dark:border-yellow-700 bg-yellow-50 dark:bg-yellow-900/20"
                          : "border-gray-300 dark:border-gray-700 bg-gray-50 dark:bg-gray-800/50"
                      }`}
                    >
                      <div className="flex items-start justify-between mb-3">
                        <div className="flex items-center space-x-3">
                          <div
                            className={`h-8 w-8 rounded-full flex items-center justify-center ${
                              step.priority === "high"
                                ? "bg-blue-100 dark:bg-blue-900/30 text-blue-600 dark:text-blue-400"
                                : step.priority === "medium"
                                ? "bg-yellow-100 dark:bg-yellow-900/30 text-yellow-600 dark:text-yellow-400"
                                : "bg-gray-100 dark:bg-gray-800 text-gray-600 dark:text-gray-400"
                            }`}
                          >
                            {index + 1}
                          </div>
                          <h3 className="font-bold">{step.action}</h3>
                        </div>
                        <span
                          className={`px-3 py-1 rounded-full text-xs font-medium ${
                            step.priority === "high"
                              ? "bg-red-100 dark:bg-red-900/30 text-red-700 dark:text-red-400"
                              : step.priority === "medium"
                              ? "bg-yellow-100 dark:bg-yellow-900/30 text-yellow-700 dark:text-yellow-400"
                              : "bg-green-100 dark:bg-green-900/30 text-green-700 dark:text-green-400"
                          }`}
                        >
                          {step.priority} priority
                        </span>
                      </div>
                      <div className="flex items-center space-x-2 text-sm text-gray-600 dark:text-gray-400">
                        <Clock className="h-4 w-4" />
                        <span>Timeline: {step.timeline}</span>
                      </div>
                    </div>
                  ))}
                </motion.div>
              )}
            </AnimatePresence>
          </div>

          {/* Right Column - CTA & Actions */}
          <div className="space-y-6">
            {/* Smart CTA */}
            <div className="p-6 rounded-2xl bg-gradient-to-br from-green-50 to-emerald-50 dark:from-green-900/20 dark:to-emerald-900/20">
              <h3 className="text-xl font-bold mb-4">Ready to Move Forward?</h3>
              <p className="text-gray-700 dark:text-gray-300 mb-6">
                Let's discuss how to turn this blueprint into reality
              </p>

              <div className="space-y-3">
                <Link
                  href="/booking"
                  className="block w-full py-3 px-6 bg-gradient-to-r from-blue-600 to-cyan-500 text-white font-semibold rounded-lg hover:from-blue-700 hover:to-cyan-600 transition-all text-center"
                >
                  <Calendar className="h-5 w-5 inline mr-2" />
                  Book Free Strategy Call
                </Link>

                <button className="w-full py-3 px-6 bg-green-600 text-white font-semibold rounded-lg hover:bg-green-700 transition-colors flex items-center justify-center">
                  <MessageSquare className="h-5 w-5 mr-2" />
                  Chat on WhatsApp
                </button>

                <button
                  onClick={handleDownload}
                  className="w-full py-3 px-6 border border-gray-300 dark:border-gray-700 rounded-lg hover:bg-gray-50 dark:hover:bg-gray-800 transition-colors flex items-center justify-center"
                >
                  <Download className="h-5 w-5 mr-2" />
                  Download Full Report
                </button>
              </div>
            </div>

            {/* Need Help */}
            <div className="p-6 rounded-2xl bg-gradient-to-br from-blue-50 to-cyan-50 dark:from-blue-900/20 dark:to-cyan-900/20">
              <h3 className="text-xl font-bold mb-4">Need Help?</h3>
              <div className="space-y-4">
                <a
                  href="mailto:hello@hassan.dev"
                  className="flex items-center space-x-3 p-3 rounded-lg bg-white dark:bg-gray-900 hover:bg-gray-100 dark:hover:bg-gray-800 transition-colors"
                >
                  <Mail className="h-5 w-5 text-blue-600" />
                  <div>
                    <div className="font-medium">Email Support</div>
                    <div className="text-sm text-gray-600 dark:text-gray-400">
                      hello@hassan.dev
                    </div>
                  </div>
                </a>

                <Link
                  href="/contact"
                  className="flex items-center space-x-3 p-3 rounded-lg bg-white dark:bg-gray-900 hover:bg-gray-100 dark:hover:bg-gray-800 transition-colors"
                >
                  <MessageSquare className="h-5 w-5 text-green-600" />
                  <div>
                    <div className="font-medium">Contact Form</div>
                    <div className="text-sm text-gray-600 dark:text-gray-400">
                      Detailed project inquiry
                    </div>
                  </div>
                </Link>
              </div>
            </div>

            {/* Generated Info */}
            <div className="p-4 rounded-xl bg-gray-50 dark:bg-gray-800/50">
              <div className="text-sm text-gray-600 dark:text-gray-400">
                Blueprint generated on{" "}
                {new Date(blueprint.generatedAt).toLocaleDateString("en-US", {
                  weekday: "long",
                  year: "numeric",
                  month: "long",
                  day: "numeric",
                  hour: "2-digit",
                  minute: "2-digit",
                })}
              </div>
              <div className="text-xs text-gray-500 dark:text-gray-500 mt-1">
                ID: {blueprint.id}
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}
